

//
//Core Java Design Principles
//1. DRY (Don't Repeat Yourself)
//        Avoid code duplication.
//
//        Extract common logic into reusable methods or classes.
//
//        2. KISS (Keep It Simple, Stupid)
//Keep your code as simple and readable as possible.
//
//Avoid over-engineering.
//
//3. YAGNI (You Aren’t Gonna Need It)
//Don’t add functionality until it’s necessary.
//
//Keep code lean and focused on current requirements.
//
//        4. SRP (Single Responsibility Principle)
//A class should have only one reason to change.
//
//        5. OCP (Open/Closed Principle)
//Classes should be open for extension but closed for modification.
//
//6. LSP (Liskov Substitution Principle)
//Subclasses must be usable in place of their superclass without breaking the app.
//
//7. ISP (Interface Segregation Principle)
//        Create specific, small interfaces instead of one big interface.
//
//        8. DIP (Dependency Inversion Principle)
//High-level modules should not depend on low-level modules. Both should depend on abstractions.


